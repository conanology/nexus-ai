/**
 * Simplified World Map SVG Path Data
 *
 * Contains recognizable silhouettes for ~50 major countries.
 * Equirectangular projection scaled to 1920x1080.
 *
 * Projection formula:
 *   x = (lon + 180) / 360 * 1920
 *   y = (90 - lat) / 180 * 1080
 *
 * Source: Manually simplified from Natural Earth boundaries (public domain).
 * These are approximate shapes â€” recognizable silhouettes, not survey-grade.
 */

export interface CountryPath {
  code: string;
  name: string;
  path: string;
  center: [number, number]; // [x, y] in SVG coordinates
}

// ---------------------------------------------------------------------------
// Projection helpers
// ---------------------------------------------------------------------------

function lonToX(lon: number): number {
  return Math.round((lon + 180) / 360 * 1920);
}

function latToY(lat: number): number {
  return Math.round((90 - lat) / 180 * 1080);
}

function ringToPath(ring: number[][]): string {
  return ring
    .map(([lon, lat], i) => `${i === 0 ? 'M' : 'L'}${lonToX(lon)},${latToY(lat)}`)
    .join(' ') + ' Z';
}

function ringsToPath(rings: number[][][]): string {
  return rings.map(ringToPath).join(' ');
}

